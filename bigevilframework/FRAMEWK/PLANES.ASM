	; Address bit pattern: --DC BA98 7654 3210 ---- ---- ---- --FE
	move.l  #vram_addr_plane_a, d1	; Add VRAM address offset
	rol.l   #0x2, d1				; Roll bits 14/15 of address to bits 16/17
	lsr.w   #0x2, d1				; Shift lower word back
